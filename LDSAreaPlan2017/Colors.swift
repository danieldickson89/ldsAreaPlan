import Foundation
import UIKit

extension UIColor {
    
    static func myBlueColor() -> UIColor {
        return UIColor(red: 0.597, green: 0.760, blue: 1.000, alpha: 1.00)
    }
    
    static func myRedColor() -> UIColor {
        return UIColor(red: 1.000, green: 0.764, blue: 0.740, alpha: 1.00)
    }
}
